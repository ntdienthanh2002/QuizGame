    public ArrayList<QA> SearchList() {
        String value = jTextFieldSearch.getText();
        createConnection();
        PreparedStatement ps = null;
        ResultSet rs = null;
        Statement st = null;
        ArrayList<QA> SearchList = new ArrayList<>();
        try {
            String sql1 = "SELECT * FROM Questions WHERE CONCAT(QuesID, Question, Answer1, Answer2, Answer3, Answer4) LIKE '%" + value + "%'";
            st = conn.createStatement();
            rs = st.executeQuery(sql1);
            QA qa;

            while (rs.next()) {
                qa = new QA(rs.getInt("QuesID"), rs.getString("Question"), rs.getString("Answer1"), rs.getString("Answer2"), rs.getString("Answer3"), rs.getString("Answer4"), rs.getString("RightAnswer"), rs.getInt("Level"));
                SearchList.add(qa);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return SearchList;
    }

