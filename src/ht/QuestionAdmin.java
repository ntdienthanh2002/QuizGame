    public ArrayList<QA> QAList() {
        createConnection();
        PreparedStatement ps = null;
        ResultSet rs = null;
        Statement st = null;
        ArrayList<QA> QAList = new ArrayList<>();
        try {
            String sql1 = "SELECT * FROM Questions";
            st = conn.createStatement();
            rs = st.executeQuery(sql1);
            QA qa;

            while (rs.next()) {
                qa = new QA(rs.getInt("QuesID"), rs.getString("Question"), rs.getString("Answer1"), rs.getString("Answer2"), rs.getString("Answer3"), rs.getString("Answer4"), rs.getString("RightAnswer"), rs.getInt("Level"));
                QAList.add(qa);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return QAList;
    }

    public void show_QA() {
        ArrayList<QA> list = QAList();
        DefaultTableModel model = (DefaultTableModel) jTableQuestionAndAnswer.getModel();
        Object[] row = new Object[8];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getQuesID();
            row[1] = list.get(i).getQuestion();
            row[2] = list.get(i).getAnswer1();
            row[3] = list.get(i).getAnswer2();
            row[4] = list.get(i).getAnswer3();
            row[5] = list.get(i).getAnswer4();
            row[6] = list.get(i).getRightAnswer();
            row[7] = list.get(i).getLevel();
            model.addRow(row);
        }
    }

