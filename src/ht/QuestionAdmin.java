    private void jTableQuestionAndAnswerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableQuestionAndAnswerMouseClicked
        UpdateQuestion jtRowData = new UpdateQuestion();

        int i = jTableQuestionAndAnswer.getSelectedRow();
        TableModel model = jTableQuestionAndAnswer.getModel();
        String id = model.getValueAt(i, 0).toString();
        String question = model.getValueAt(i, 1).toString();
        String answer1 = model.getValueAt(i, 2).toString();
        String answer2 = model.getValueAt(i, 3).toString();
        String answer3 = model.getValueAt(i, 4).toString();
        String answer4 = model.getValueAt(i, 5).toString();
        String rightanswer = model.getValueAt(i, 6).toString();
        String level = model.getValueAt(i, 7).toString();

        jtRowData.setVisible(true);
        jtRowData.setLocationRelativeTo(null);
        this.setVisible(false);
        jtRowData.pack();
        jtRowData.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

        jtRowData.jLabelID.setText(id);
        jtRowData.jTextFieldQuestion.setText(question);
        jtRowData.jTextFieldA.setText(answer1.substring(3));
        jtRowData.jTextFieldB.setText(answer2.substring(3));
        jtRowData.jTextFieldC.setText(answer3.substring(3));
        jtRowData.jTextFieldD.setText(answer4.substring(3));
        switch (rightanswer) {
            case "A":
                jtRowData.jComboBoxRightAnswer.setSelectedIndex(0);
                break;
            case "B":
                jtRowData.jComboBoxRightAnswer.setSelectedIndex(1);
                break;
            case "C":
                jtRowData.jComboBoxRightAnswer.setSelectedIndex(2);
                break;
            case "D":
                jtRowData.jComboBoxRightAnswer.setSelectedIndex(3);
                break;
        }
        switch (level) {
            case "1":
                jtRowData.jComboBoxLevel.setSelectedIndex(0);
                break;
            case "2":
                jtRowData.jComboBoxLevel.setSelectedIndex(1);
                break;
            case "3":
                jtRowData.jComboBoxLevel.setSelectedIndex(2);
                break;
        }
    }//GEN-LAST:event_jTableQuestionAndAnswerMouseClicked

