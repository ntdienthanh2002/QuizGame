    public ArrayList<AP> SearchListAP() {
        createConnection();
        String value = jTextFieldSearch.getText();
        PreparedStatement ps = null;
        ResultSet rs = null;
        Statement st = null;
        ArrayList<AP> SearchListAP = new ArrayList<>();
        try {
            String sql1 = "SELECT * FROM AccountPlayer WHERE CONCAT(AccPlayerID, Phone, Email, Fullname, Gender, Status) LIKE '%" + value + "%'";
            st = conn.createStatement();
            rs = st.executeQuery(sql1);
            AP ap;

            while (rs.next()) {
                ap = new AP(rs.getInt("AccPlayerID"), rs.getInt("Phone"), rs.getString("Email"), rs.getString("Fullname"), rs.getString("Gender"), rs.getInt("Status"));
                SearchListAP.add(ap);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return SearchListAP;
    }

    public void show_SearchAP() {
        ArrayList<AP> list = SearchListAP();
        DefaultTableModel model = (DefaultTableModel) jTableAccountPlayer.getModel();
        Object[] row = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getAccPlayerID();
            row[1] = list.get(i).getPhone();
            row[2] = list.get(i).getEmail();
            row[3] = list.get(i).getFullname();
            row[4] = list.get(i).getGender();
            row[5] = list.get(i).getStatus();
            model.addRow(row);
        }
    }

