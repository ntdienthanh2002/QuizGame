    private void jButtonUnBlockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUnBlockActionPerformed
        createConnection();
        DefaultTableModel model = (DefaultTableModel) jTableAccountPlayer.getModel();
        int i = jTableAccountPlayer.getSelectedRow();
        String id = model.getValueAt(i, 0).toString();
        int value = Integer.parseInt(id);
        JFrame frame = new JFrame();
        int n = JOptionPane.showConfirmDialog(frame, "Do you want to unblock this account?", "System", JOptionPane.YES_NO_OPTION);
        if (n == 0) {
            try {
                String query = "UPDATE AccountPlayer SET Status=1 WHERE AccPlayerID =" + value;
                PreparedStatement ps = conn.prepareStatement(query);
                ps.executeUpdate();
            } catch (Exception e) {
                e.printStackTrace();
            }
            //load lai bảng
            model.setRowCount(0);
            show_AP();
        }
    }//GEN-LAST:event_jButtonUnBlockActionPerformed

