    private void jButtonChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChangePasswordActionPerformed
        createConnection();
        String op = jTextFieldOldPassword.getText();
        String np = jTextFieldNewPassword.getText();
        String value = Tam.tam;
        StringBuilder sb = new StringBuilder();
        if (op.length() < 4) {
            sb.append("New Password not less than 4 characters !\n");
        }
        if (op.equals("")) {
            sb.append("Please check New Password can't be empty !\n");
        }
        if (np.equals("")) {
            sb.append("Please check Old Password can't be empty !\n");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "System", JOptionPane.ERROR_MESSAGE);
        } else {
            try {

                String query = "SELECT * FROM AccountAdmin WHERE Username = '" + value + "' AND Password=?";

                PreparedStatement ps = conn.prepareStatement(query);

                ps.setString(1, op);
                ResultSet rs = null;
                rs = ps.executeQuery();
                if (rs.next()) {
                    String query1 = "UPDATE AccountAdmin SET Password=? WHERE Username ='" + value + "'";
                    PreparedStatement ps1 = conn.prepareStatement(query1);
                    ps1.setString(1, np);
                    ps1.executeUpdate();

                    JFrame frame = new JFrame("JOptionPane showMessageDialog example");
                    JOptionPane.showMessageDialog(frame,
                            "Change password successfully !");

                } else {
                    JFrame frame = new JFrame("JOptionPane showMessageDialog example");
                    JOptionPane.showMessageDialog(frame,
                            "Wrong password !",
                            "System",
                            JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButtonChangePasswordActionPerformed

